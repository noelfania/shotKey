/** 구두점·괄호 플릭 문자 (히라/카타 출제 풀 공통) */
export const punctuationChars = ["、", "。", "?", "!", "「", "」", "ー"] as const;

/**
 * 스크립트별 출제 풀을 반환한다. (50음 + 구두점)
 */
export function getGojuonPool(script: KanaScript): readonly string[] {
  const base = script === "hiragana" ? hiraganaGojuon : katakanaGojuon;
  return [...base, ...punctuationChars];
}
